        <h1 id="introduction">Introduction</h1>
<p>Dokumentasi endpoint API untuk backend Zaku.</p>
<aside>
    <strong>Base URL</strong>: <code>http://127.0.0.1:8000</code>
</aside>
<pre><code>Gunakan dokumentasi ini untuk mencoba endpoint API langsung dari browser, menyalin contoh request, atau mengunduh Postman collection dan OpenAPI spec.

&lt;aside&gt;Untuk endpoint yang membutuhkan login, isi token JWT dari endpoint login pada tombol Authenticate.&lt;/aside&gt;</code></pre>

        <h1 id="authenticating-requests">Authenticating requests</h1>
<p>To authenticate requests, include an <strong><code>Authorization</code></strong> header with the value <strong><code>"Bearer {YOUR_JWT_TOKEN}"</code></strong>.</p>
<p>All authenticated endpoints are marked with a <code>requires authentication</code> badge in the documentation below.</p>
<p>Dapatkan token dari endpoint <code>POST /api/auth/login</code>, lalu kirim sebagai Bearer token.</p>

